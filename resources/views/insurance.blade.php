                        <h5 class="h5-md steelblue-color">Hospitalization benefit for TPA Patients</h5>

                        <p>Patients can avail the benefit of Cashless hospitalization at Bombay Hospital Indore if the TPA that the patient is covered by is tied up with the hospital.</p>

                        <h5 class="h5-md steelblue-color">How to identify your TPA?</h5>

                        <p>There are several ways to identify your TPA:</p>

                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p>The TPA will issue you an identity card which will indicate the name of your TPA.
                            </p>
                        </div>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p>On your insurance policy, on the lower left hand corner of the policy, the name of the TPA will be printed.
                            </p>
                        </div>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p>Your insurance agent will be able to inform you accurately.
                            </p>
                        </div>

                        <h5 class="h5-md steelblue-color">Where to get TPA Pre authorization forms?</h5>

                        <p>The pre authorization forms of TPA tied up with the hospital can be collected from the marketing department.</p>

                        <h5 class="h5-md steelblue-color">Important points to Note:</h5>

                        <p>Possessing a TPA identity card does not automatically entitle the patient to get Cashless benefit. A certain defined procedure for obtaining sanction from the TPA has to be followed.</p>

                        <h5 class="h5-md steelblue-color">Admission process:</h5>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p>For all elective / planned admissions, Cashless approval should be obtained from the TPA atleast 2 days prior to the date of admission.
                            </p>
                        </div>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p>Marketing department will assist you in faxing the duly filled pre authorization form of the TPA (along with the latest policy copy and positive investigation reports.)
                            </p>
                        </div>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p>At the time of admission, even if the TPA has approved cashless, a refundable security deposit of Rs. 5000/- is to be paid by the patient.
                            </p>
                        </div>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p>Incase the bed class / room specified in the authorization letter is not available, the patient will be admitted in the higher or lower class as per choice of patient. In case of admission in the higher class, patient will have to pay the difference in the charges from the date of admission.
                            </p>
                        </div>

                        <h5 class="h5-md steelblue-color">During hospitalization:</h5>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p>The patient must give a proper and true medical history to the doctor at the time of filling in the pre authorization form, since any discrepancy may result in the denial of the claim and cancellation of the approval.
                            </p>
                        </div>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p>In case the patient is transferred to ICU/ICCU/ Recovery room and wants to retain the original bed then patient will have to keep an additional security deposit, depending on the class and number of days in ICU/ICCU/Recovery Room, as the TPA does not pay for dual occupancy.
                            </p>
                        </div>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p>Receiving an authorization letter for Cashless benefit is only tentative. If the claim is denied for any reason by the insurance company during the course of treatment or after patient is discharged, patient will be responsible for payment of the hospital bills, as per the undertaking which the hospital collects at the time of registration under respective TPA.
                            </p>
                        </div>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p>If the patient bill exceeds the sanctioned amount, an estimate for the additional amount will be sent through the marketing department to the TPA. However, it is primarily the responsibility of the patient / relative to keep track of the bill. If no enhancement is received from the TPA, the patient will have to pay for the bill amount over and above the sanctioned amount by the TPA.
                            </p>
                        </div>

                        <h5 class="h5-md steelblue-color">Discharge process:</h5>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p>The hospital will retain Rs. 5000/- refundable deposit for every one lakh of the sanctioned amount as deposit until the hospital receives complete payment from the TPA. On settlement of payment from the TPA, the patient will be intimated by SMS regarding procedure of refund of deposit.
                            </p>
                        </div>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p>The hospital shall collect payment for all exclusions / non-medical expenses / surcharge, if they are not covered by TPA at the time of discharge.
                            </p>
                        </div>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p>No original hospital document’s or reports will be given to patient, since these are sent by the hospital to TPA along with the bill for reimbursement. Xerox copies of the same can be taken by the relative at his own cost.
                            </p>
                        </div>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p>At the time of Discharge, the patient can collect a copy of detailed bill / summary from the accounts department.
                            </p>
                        </div>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p>Any pending reports or films should be collected from the marketing department within 10 days of discharge.
                            </p>
                        </div>

                        <h5 class="h5-md steelblue-color">Denial process:</h5>
                        <p>On denial of Cashless hospitalization by the TPA, the patient will be treated as a cash paying patient and will have to make the necessary payments as per hospital policy.</p>

                        <h5 class="h5-md steelblue-color">For Reimbursement:</h5>
                        <p>For patients who do not avail the benefit of Cashless hospitalization or whose claims are denied for Cashless, please contact your respective TPA along with patient’s Discharge card, all Original reports, claim form of insurance company and hospital bills (to be attested by Medical Suptd. / Officer on Special Duty) for reimbursement. If the Indoor case papers are required, kindly make an application in the Medical Suptd. Office / OSD Office during working hours.</p>

                        <p><b>Bombay Hospital Indore Reserves the right to deny the Cashless Facility to any of the Card Holders of Above Mentioned TPA’s.</b></p>

                        <p>Marketing department will assist you and guide you in the process of obtaining the cashless facility from the TPA / Insurance company.<b>However the primary responsibility of obtaining the authorisation would be of the patient / relative.</b></p>

