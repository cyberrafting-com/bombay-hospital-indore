                        <p>Bombay Hospital Indore has been certified as a baby-friendly hospital and always believes
                            that patient care is a priority. The Hospital for years has strived to develop one of the
                            best neonatal services in the city and is currently accredited as a tertiary level unit. The
                            aim of the department has always been to provide optimal newborn care at minimal cost to the
                            parents and yet maintain a high level of standard. Infection rates have been one of the
                            lowest in the country. With the help of supportive services the unit attempts to achieve
                            intact survival with minimum morbidity in its babies.</p>

                        <h5 class="h5-md steelblue-color">Antenatal care-</h5>

                        <p>The unit is very concerned about its potential clients and takes great pains to collect
                            information about normal as well as high risk pregnancies. Not infrequently an expectant
                            mother is referred to the hospital for anticipatory NICU care from other hospitals and
                            nursing homes. Forewarned is forearmed is the dictum followed in the unit. Our obstetricians
                            also believe in liaising with us which is reflected by the high usage of antenatal steroids
                            in threatened premature labour. Because of this fact the usage of assisted ventilation and
                            surfactant therapy is at its minimum.</p>

                        <h5 class="h5-md steelblue-color">Intrapartum care-</h5>

                        <p>All deliveries in the hospital are attended by a paediatric resident and high risk situations
                            are attended to by the consultant. We have a transport system to transfer sick babies after
                            stabilization to the NICU for subsequent care. All pediatric residents are well versed in
                            neonatal resuscitation.</p>

                        <h5 class="h5-md steelblue-color">NICU care-</h5>

                        <p>The NICU is equipped with the state of the art monitoring system. The unit has adequate
                            numbers of syringe pumps, pulsoximeters, micromethods for blood report estimation and a good
                            patient to nurse ratio. Every baby is closely observed and steps taken to make its stay
                            comfortable. Bombay Hospital Indore is one of the first hospitals to have established a
                            systematic ventilatory program which includes a high frequency option too. High frequency
                            ventilation is used in situations of congenital diaphragmatic hernia, and refractory
                            pulmonary hypertension. Babies are exposed to early intervention program before discharge.
                        </p>

                        <h5 class="h5-md steelblue-color">Postnatal care-</h5>

                        <p>The concept of keeping baby and mother together has been well established and we try to
                            maintain the same. Breast feeding is encouraged from the first hour of life and supervision
                            by our lactation nurse ensures adequate and fruitful lactation. Vaccination is initiated in
                            the hospital itself and followed up in our immunization clinic.</p>

