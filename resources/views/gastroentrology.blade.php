                        <p>The department provides a comprehensive gastroenterology service with the help of
                            modern technology and a humane touch. The gastroenterology department has a highly
                            qualified consultant and also is a prefered refferal centre and patients are
                            referred from all over central india.</p>

                        <h5 class="h5-md steelblue-color">Medical Gastroenterology and Hepatology</h5>

                        <p>The department deals with various medical problems in gastroenterology in the
                            outpatient, inpatient, and emergency services . The doctor has expertise in the
                            management of the full spectrum of gastrointestinal, liver and pancreatico-biliary
                            diseases. This includes, but is not restricted to, disorders like inflammatory bowel
                            disease, irritable bowel syndrome, malabsorption syndrome, gastrointestinal bleed,
                            chronic diarrhea, constipation, peptic ulcer, reflux disease, acute liver failure,
                            chronic liver disease (viral, alcoholic and others), portal hypertension,
                            bilio-pancreatic disorders , malignancies etc.</p>

                        <h5 class="h5-md steelblue-color">Interventional Gastroenterology</h5>

                        <p>Bombay Hospital Indore has a well-equipped endoscopy department where diagnostic,
                            therapeutic as well as emergency procedures in gastroenterology are performed. The
                            department also performs procedures like percutaneous and trans-jugular liver biopsy
                            and Liver Abscess Drainage. The procedures most commonly performed include:</p>

                        <h5 class="h5-md steelblue-color">UGI Endoscopy:</h5>

                        <p>Examination of the upper gastrointestinal tract using a flexible video-endoscope.</p>

                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p><b>Diagnostic –</b> for disorders like acid-peptic disease, H. pylori infection,
                                gastro-esophageal reflux disease, cancers of the esophagus and stomach.
                            </p>
                        </div>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p><b>Therapeutic –</b> for management of emergencies like variceal bleeding
                                (procedures like sclerotherapy, variceal banding), endoscopic therapy for
                                bleeding peptic ulcer by injection, application of hemoclips or thermal current,
                                placement of percutaneous endoscopic gastrostomy (PEG) tube or jejunostomy as a
                                feeding device for neurologically impaired individuals or those who are unable
                                to swallow. We also perform endotherapy for early cancers and place stents for
                                palliation of obstructive malignant lesions.
                            </p>
                        </div>

                        <h5 class="h5-md steelblue-color">Colonoscopy:</h5>

                        <p>Examination of the lower gastrointestinal tract using a flexible video-endoscope.</p>

                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p><b>Diagnostic –</b> for disorders like polyps, cancers, strictures, bleeding sites,
                                diverticuli.
                            </p>
                        </div>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p><b>Therapeutic –</b> for management of colonic obstruction with stenting, endotherapy for
                                lower gastrointestinal bleeding, and polypectomy.
                            </p>
                        </div>

                        <h5 class="h5-md steelblue-color">ERCP (Endoscopic retrograde cholangio pancreatography):</h5>

                        <p>This procedure involves treating various Biliary-pancreatic disorders. These include
                            obstructive jaundice caused by common bile duct stones (extraction of the stones) or
                            relieving obstruction in biliary-pancreatic cancers by placement of plastic or metal stents.
                            ERCP also aids in obtaining tumor tissue cytology / biopsy from the bile duct to confirm the
                            diagnosis in suspected cases of malignancy. helpful in procuring tissue for examination. It
                            aids in difficult biliary and pancreatic therapeutic endoscopic procedures.</p>
