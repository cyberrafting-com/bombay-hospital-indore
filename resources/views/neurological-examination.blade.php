                        <h5 class="h5-md steelblue-color">What is a neurological examination?</h5>
                        <p>A neurological examination, also called a neuro exam, is an evaluation of a person’s nervous
                            system that can be performed in the doctor’s office. It may be performed with instruments,
                            such as lights and reflex hammers, and usually does not cause any pain to the patient. The
                            nervous system consists of the brain, the spinal cord, and the nerves from these areas.
                            There are many aspects of this examination, including an assessment of motor and sensory
                            skills, balance and coordination, mental status (the patient’s level of awareness and
                            interaction with the environment), reflexes, and functioning of the nerves. The extent of
                            the examination depends on many factors, including the initial problem that the patient is
                            experiencing, the age of the patient, and the condition of the patient.</p>

                        <h5 class="h5-md steelblue-color">Why is a neurological examination performed?</h5>
                        <p>A complete and thorough evaluation of a person’s nervous system is important if there is any
                            reason to think there may be an underlying problem, or during a complete physical. Damage to
                            the nervous system can cause problems in daily functioning. Early identification may help to
                            identify the cause and decrease long-term complications. A complete neurological examination
                            may be performed:</p>

                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p>During a routine physical</p>
                        </div>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p>Following any type of trauma</p>
                        </div>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p>To follow the progression of a disease</p>
                        </div>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p>If the person has any of the following complaints:</p>
                        </div>

                        <ul class="content--list">
                            <li>Headaches</li>
                            <li>Blurry vision</li>
                            <li>Change in behavior</li>
                            <li>Fatigue</li>
                            <li>Change in balance or coordination</li>
                            <li>Numbness or tingling in the arms or legs</li>
                            <li>Decrease in movement of the arms or legs</li>
                            <li>Injury to the head, neck, or back</li>
                            <li>Fever</li>
                            <li>Seizures</li>
                            <li>Slurred speech</li>
                            <li>Weakness</li>
                            <li>Tremor</li>
                        </ul>

                        <h5 class="h5-md steelblue-color">What is done during a neurological examination?</h5>
                        <p>During a neurological examination, the doctor will “test” the functioning of the nervous
                            system. The nervous system is very complex and controls many parts of the body. The nervous
                            system consists of the brain, spinal cord, 12 nerves that come from the brain, and the
                            nerves that come from the spinal cord. The circulation to the brain, arising from the
                            arteries in the neck, is also frequently examined. In infants and younger children, a
                            neurological examination includes the measurement of the head circumference. The following
                            is an overview of some of the areas that may be tested and evaluated during a neurological
                            examination:</p>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p><b>Mental status:</b> Mental status (the patient’s level of awareness and interaction
                                with the environment) may be assessed by conversing with the patient and establishing
                                his or her awareness of person, place, and time. The person will also be observed for
                                clear speech and making sense while talking. This is usually done by the patient’s
                                doctor just by observing the patient during normal interactions.</p>
                        </div>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p><b>Motor function and balance:</b> This may be tested by having the patient push and pull
                                against the doctor’s hands with his or her arms and legs. Balance may be checked by
                                assessing how the person stands and walks or having the patient stand with his or her
                                eyes closed while being gently pushed to one side or the other. The patient’s joints may
                                also be checked simply by passive (performed by the doctor) and active (performed by the
                                patient) movement.</p>
                        </div>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p><b>Sensory examination:</b> The patient’s doctor may also perform a sensory test that
                                checks his or her ability to feel. This may be done by using different instruments: dull
                                needles, tuning forks, alcohol swabs, or other objects. The doctor may touch the
                                patient’s legs, arms, or other parts of the body and have him or her identify the
                                sensation (for example, hot or cold, sharp or dull).</p>
                        </div>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p><b>Newborn and infant reflexes:</b> There are different types of reflexes that may be
                                tested. In newborns and infants, reflexes called infant reflexes (or primitive reflexes)
                                are evaluated. Each of these reflexes disappears at a certain age as the infant grows.
                                These reflexes include:</p>
                        </div>
                        <ul class="content--list">
                            <li><b>Blinking</b> An infant will close his or her eyes in response to bright lights.</li>
                            <li><b>Babinski reflex</b> As the infant’s foot is stroked, the toes will extend upward.
                            </li>
                            <li><b>Crawling</b> If the infant is placed on his or her abdomen, he or she will make
                                crawling motions.</li>
                            <li><b>Moro’s reflex (or startle reflex)</b> A quick change in the infant’s position will
                                cause the infant to throw the arms outward, open the hands, and throw back the head.
                            </li>
                            <li><b>Palmar and plantar grasp</b> The infant’s fingers or toes will curl around a finger
                                placed in the area.</li>
                        </ul>


                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p><b>Reflexes in the older child and adult:</b> These are usually examined with the use of
                                a reflex hammer. The reflex hammer is used at different points on the body to test
                                numerous reflexes, which are noted by the movement that the hammer causes.</p>
                        </div>
                        <div class="box-list">
                            <div class="box-list-icon"><i class="fas fa-angle-double-right"></i></div>
                            <p><b>Evaluation of the nerves of the brain:</b> There are 12 main nerves of the brain,
                                called the cranial nerves. During a complete neurological examination, most of these
                                nerves are evaluated to help determine the functioning of the brain:</p>
                        </div>

                        <ul class="content--list">
                            <li><b>Cranial nerve I (olfactory nerve):</b> This is the nerve of smell. The patient may be
                                asked to identify different smells with his or her eyes closed.</li>
                            <li><b>Cranial nerve II (optic nerve):</b> This nerve carries vision to the brain. A visual
                                test may be given and the patient’s eye may be examined with a special light.</li>
                            <li><b>Cranial nerve III (oculomotor):</b> This nerve is responsible for pupil size and
                                certain movements of the eye. The patient’s doctor may examine the pupil (the black part
                                of the eye) with a light and have the patient follow the light in various directions.
                            </li>
                            <li><b>Cranial nerve IV (trochlear nerve):</b> This nerve also helps with the movement of
                                the eyes.</li>
                            <li><b>Cranial nerve V (trigeminal nerve):</b> This nerve allows for many functions,
                                including the ability to feel the face, inside the mouth, and move the muscles involved
                                with chewing. The patient’s doctor may touch the face at different areas and watch the
                                patient as he or she bites down.</li>
                            <li><b>Cranial nerve VI (abducens nerve):</b> This nerve helps with the movement of the
                                eyes. The patient may be asked to follow a light or finger to move the eyes.</li>
                            <li><b>Cranial nerve VII (facial nerve):</b> This nerve is responsible for various
                                functions, including the movement of the face muscle and taste. The patient may be asked
                                to identify different tastes (sweet, sour, bitter), asked to smile, move the cheeks, or
                                show the teeth.</li>
                            <li><b>Cranial nerve VIII (acoustic nerve):</b> This nerve is the nerve of hearing. A
                                hearing test may be performed on the patient.</li>
                            <li><b>Cranial nerve IX (glossopharyngeal nerve):</b> This nerve is involved with taste and
                                swallowing. Once again, the patient may be asked to identify different tastes on the
                                back of the tongue. The gag reflex may be tested.</li>
                            <li><b>Cranial nerve X (vagus nerve):</b> This nerve is mainly responsible for the ability
                                to swallow, the gag reflex, some taste, and part of speech. The patient may be asked to
                                swallow and a tongue blade may be used to elicit the gag response.</li>
                            <li><b>Cranial nerve XI (accessory nerve):</b> This nerve is involved in the movement of the
                                shoulders and neck. The patient may be asked to turn his or her head from side to side
                                against mild resistance, or to shrug the shoulders.</li>
                            <li><b>Cranial nerve XII (hypoglossal nerve):</b> The final cranial nerve is mainly
                                responsible for movement of the tongue. The patient may be instructed to stick out his
                                or her tongue and speak.</li>
                        </ul>

