                        <p>Electromyography & Nerve Conduction Study (EMG & NCS) also known as Electrodiagnosis, is an
                            assessment of a patient by a physician using a series of tests to establish an accurate
                            diagnosis of a presenting clinical problem that suggests a neuromuscular disorder.</p>

                        <p>It is an extension of the clinical examination to assess the functional status of the
                            peripheral neuromuscular system and is useful in the diagnosis of Peripheral Neuropathies,
                            Cranial neuropathies, Radiculopathies, Neuromuscular junction effects, Myopathies, Nerve
                            Injuries, Plexopathies and Anterior Horn cell diseases.</p>

                        <p>It is the only test that examines the function of the nervous tissue being examined and is
                            directed by the clinical impression and the working diagnosis (customized for every patient)
                        </p>

