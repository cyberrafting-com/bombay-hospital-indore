                        <h5 class="h5-md steelblue-color">What is hysterectomy?</h5>
                        <p>Hysterectomy is the surgical removal of the uterus. Different portions of the uterus, as well
                            as other organs, may be removed at the same time.</p>

                        <h5 class="h5-md steelblue-color">Facts about hysterectomy-</h5>
                        <p>More than 600,000 hysterectomies are performed in the U.S. each year.<br>Hysterectomy is the
                            second most common major operation in women of child bearing age.<br>The most common
                            conditions for hysterectomy are fibroid tumors, endometriosis, and uterine prolapse.</p>

                        <h5 class="h5-md steelblue-color">Reasons for having a hysterectomy-</h5>
                        <p>The following are several possible causes or reasons for hysterectomy:</p>
                        <p><b>Fibroid tumors-</b> Non-malignant tumors may grow and become large, causing pressure on
                            other organs and possibly heavy bleeding or pelvic pain.</p>
                        <p><b>Endometriosis-</b> Endometrial cells sometimes grow outside of the uterus, attach
                            themselves to other organs in the pelvic cavity, and bleed each month in accordance with an
                            ovarian cycle. This can result in chronic pelvic pain, pain during sex, and prolonged or
                            heavy bleeding.</p>
                        <p><b>Endometrial hyperplasia-</b> A cause of abnormal bleeding, this over-thickening of the
                            uterine lining is often due to the presence of continuous estrogen without progesterone.
                            This is common during perimenopause when hormone levels are changing.</p>
                        <p><b>Cancer-</b> Approximately 10 percent of hysterectomies are performed to treat
                            cancerâ€”either cervical, ovarian, or endometrial.</p>
                        <p><b>Blockage of the bladder or intestines-</b> A hysterectomy may be performed if there is a
                            blockage of the bladder or intestines by the uterus or a growth.</p>

                        <h5 class="h5-md steelblue-color">What are the different types of hysterectomy?</h5>
                        <p><b>Total hysterectomy:</b> Includes the removal of the entire uterus, including the fundus
                            (the part of the uterus above the openings of the fallopian tubes) and the cervix, but not
                            the ovaries. This is the most common type of hysterectomy.</p>

                        <p><b>Hysterectomy with bilateral oophorectomy:</b> Includes the removal of one or both ovaries,
                            and sometimes the fallopian tubes, along with the uterus.</p>

                        <p><b>Radical hysterectomy:</b> Includes the removal of the uterus, cervix, the top portion of
                            the vagina, most of the tissue that surrounds the cervix in the pelvic cavity, and may
                            include the removal of the pelvic lymph nodes. This is done in some cases of cancer.</p>

                        <p><b>Supracervical hysterectomy (partial or subtotal hysterectomy):</b> Removal of the body of
                            the uterus while leaving the cervix intact.</p>

                        <h5 class="h5-md steelblue-color">What are the procedures for performing hysterectomy?</h5>
                        <p><b>Abdominal hysterectomy:</b> The uterus is removed through the abdomen via a surgical
                            incision about six to eight inches long. This procedure is most commonly used when the
                            ovaries and fallopian tubes are being removed, when the uterus is enlarged, or when disease
                            has spread to the pelvic cavity, as in endometriosis or cancer. The main surgical incision
                            can be made either vertically, from the navel down to the pubic bone, or horizontally, along
                            the top of the pubic hairline.</p>

                        <p><b>Vaginal hysterectomy:</b> The uterus is removed through the vaginal opening. This
                            procedure is most often used in cases of uterine prolapse, or when vaginal repairs are
                            necessary for related conditions. No external incision is made, which means there is no
                            visible scarring.</p>

                        <p><b>Laparoscope-assisted vaginal hysterectomy (LAVH):</b> Vaginal hysterectomy is performed
                            with the aid of a laparoscope, a thin, flexible tube containing a video camera. Thin tubes
                            are inserted through tiny incisions in the abdomen near the navel. The uterus is then
                            removed in sections through the laparoscope tube or through the vagina.</p>
                        <p>The type of hysterectomy performed and the technique used to perform the procedure will be
                            determined by your physician, based upon your particular situation.</p>

                        <p>For women who have not yet reached menopause, having a hysterectomy means that menstruation
                            will no longer occur, nor will pregnancy be possible.</p>
