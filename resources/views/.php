<h5>Pediatric Health Checkup</h5>
As the gardener nurtures a bud to blossom into a beautiful flower… parents nurture their child with same care, love and affection. Here at Bombay Hospital we lend a hand to the parents to live up their dream to raise their children &amp; ensure their healthy growth and development. Children are particularly vulnerable to infectious diseases which are easily treatable.

Sadly half of all Indian children are undernourished or suffer from easily preventable diseases such as diarrhea, pneumonia, malaria, measles, tetanus and conditions like malnutrition at any given point of time.

We strive hard to improve the quality of childcare &amp; initiating and strengthening the bonds between health, safety and child care through a family of loving and caring doctors. We try to promote &amp; maintain the broadest understating of health &amp; wellness in early childhood.

<strong>Our Mission</strong>
<ol>
	<li>Dedicated to enhance the quality of child care/Neonatal care.</li>
	<li>Our Multi disciplinary team will always be there to support your child in achieving healthy &amp; safe practices throughout.</li>
	<li>We believe not only in preventing the disease but also in creating the possible environment in which children can grow to make the nation grow.</li>
</ol>
We are here to take care of your child’s body from teeth to toe, and to keep up to our mission we need your support. We request the parents to be extra attentive to their child’s health and we suggest you to adopt our childcare health package so that your child can have the happiest and healthiest childhood.
